#include <stdio.h>

/**
* add - add two integers
* @a: first int
* @b: second int
* Return: addition of integers
*
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two integers
* @a: first int
* @b: second int
* Return: subtraction of integers
*
*/
int sub(int a, int b)
{
        return (a - b);
}

/**
* mul - multiply two integers
* @a: first int
* @b: second int
* Return: multiplication of integers
*
*/
int mul(int a, int b)
{
        return (a * b);
}

/**
* div - divide two integers
* @a: first int
* @b: second int
* Return: division of integers
*
*/
int div(int a, int b)
{
        return (a / b);
}

/**
* mod - mod two integers
* @a: first int
* @b: second int
* Return: modulo of integers
*
*/
int mod(int a, int b)
{
        return (a % b);
}
